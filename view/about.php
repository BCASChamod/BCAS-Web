        <div class="description">
            <p>BCAS was founded in 1999 with the mission of providing basic computer education and knowledge of English to equip school leavers (O/A levels) with 
                the skills they need to succeed in the modern job market. The primary goal of BCAS back then was to help find immediate employment for disparate students
                 in the private and government sectors, as well as in the Middle-East as computer operators. Thus, was born BCAS' flagship course of study, the "Access 
                 Programme," which incorporates IT and English to prepare post-AL students who are not able to gain admission to state universities. In the past 2 years, 
                 approximately 10,000 students have used BCAS to gain admission to various types of higher education institutions in the UK, including universities. The 
                 Institute was the first in Sri Lanka to obtain a franchise to prepare students for the European Computer Driving Licence (ECDL). The course was later 
                 renamed the International Computer Driving Licence (ICDL).
